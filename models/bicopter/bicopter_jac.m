function [A, B] = bicopter_jac(x, u)
    m = 1.1;
    l = 0.21;
    I = 0.0196;
    g = 9.81;

    x1 = x(1);
    x1d = x(2);
    x2 = x(3);
    x2d = x(4);
    x3 = x(5);
    x3d = x(6);
    u1 = u(1);
    u2 = u(2);

    A = zeros(6, 6);
    A(1, :) = [0, 1, 0, 0,                      0, 0];
    A(2, :) = [0, 0, 0, 0, -(cos(x3)*(u1 + u2))/m, 0];
    A(3, :) = [0, 0, 0, 1,                      0, 0];
    A(4, :) = [0, 0, 0, 0, -(sin(x3)*(u1 + u2))/m, 0];
    A(5, :) = [0, 0, 0, 0,                      0, 1];
    A(6, :) = [0, 0, 0, 0,                      0, 0];
    
    B = zeros(6, 2);
    B(1, :) = [         0,          0];
    B(2, :) = [-sin(x3)/m, -sin(x3)/m];
    B(3, :) = [         0,          0];
    B(4, :) = [ cos(x3)/m,  cos(x3)/m];
    B(5, :) = [         0,          0];
    B(6, :) = [       l/I,       -l/I];
end